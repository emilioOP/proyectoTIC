    private Usuario getUsuario(String mail, String pass) throws Exception {
        //getUsuario
        Usuario usuario = null;
        
        IniciarSesion login = new IniciarSesion(server, mail, pass);
        String url = login.getUrl();

        System.out.println(url);

        JespXML a = new JespXML(new URL(url));
        Tag tagInfo = a.leerXML();

        Tag tagID = tagInfo.getTagHijoByName("id");
        Tag tagPermiso = tagInfo.getTagHijoByName("permiso");
        Tag tagNombre = tagInfo.getTagHijoByName("nombre");
        Tag tagApellido = tagInfo.getTagHijoByName("apellido");

        int id = Integer.parseInt(tagID.getContenido());
        int id_permiso = Integer.parseInt(tagPermiso.getContenido());
        String nombre = tagNombre.getContenido();
        String apellido = tagApellido.getContenido();

        usuario = new Usuario(id, id_permiso, nombre, apellido);
        return usuario;
    }
    
