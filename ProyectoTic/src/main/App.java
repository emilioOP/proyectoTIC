    private ArrayList<Evento> getEventos(int tipo) {
        ArrayList<Evento> eventos = null;

        try {
            ListarEventos listar = new ListarEventos(server, tipo);
            JespXML a = new JespXML(new URL(listar.getUrl()));
            System.out.println(listar.getUrl());
            Tag t = a.leerXML();

            eventos = new ArrayList<>();
            int id_evento;
            String empresa, ciudad, direccion, inicio, termino;
            Tag tg_id, tg_empresa, tg_ciudad, tg_direccion, tg_inicio, tg_termino;

            Evento evento;
            for (Tag i : t.getTagsHijos()) {
                System.out.println("Hijo");

                tg_id = i.getTagHijoByName("id");
                tg_empresa = i.getTagHijoByName("empresa");
                tg_ciudad = i.getTagHijoByName("ciudad");
                tg_direccion = i.getTagHijoByName("direccion");
                tg_inicio = i.getTagHijoByName("inicio");
                tg_termino = i.getTagHijoByName("termino");

                empresa = tg_empresa.getContenido();
                ciudad = tg_ciudad.getContenido();
                direccion = tg_direccion.getContenido();
                inicio = tg_inicio.getContenido();
                termino = tg_termino.getContenido();
                id_evento = Integer.parseInt(tg_id.getContenido());

                evento = new Evento(id_evento, empresa, ciudad, inicio, termino, direccion);
                eventos.add(evento);

            }

        } catch (IOException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        } catch (TagHijoNotFoundException ex) {
            Logger.getLogger(App.class.getName()).log(Level.SEVERE, null, ex);
        }

        return eventos;
    }
    
